import curses
import time
import pyttsx3

engine= pyttsx3.init()

# 10x10 미로 입력
maze = [list(map(int, input().split())) for _ in range(10)]

result=[]
# 방향: 상, 하, 좌, 우
dx = [-1, 1, 0, 0]
dy = [0, 0, -1, 1]

start_x, start_y = 1, 1
visited = [[False] * len(maze) for _ in range(len(maze[0]))]
parent = [[None] * len(maze) for _ in range(len(maze[0]))]

queue = [(start_x, start_y)]
visited[start_x][start_y] = True

found = False
end_x, end_y = -1, -1

# BFS로 최단 경로 찾기
while queue:
    x, y = queue.pop(0)

    if maze[x][y] == 2:
        end_x, end_y = x, y
        found = True
        break

    for i in range(4):
        nx, ny = x + dx[i], y + dy[i]

        if 0 <= nx < len(maze) and 0 <= ny < len(maze[0]):
            if not visited[nx][ny] and maze[nx][ny] != 1:
                visited[nx][ny] = True
                parent[nx][ny] = (x, y)
                queue.append((nx, ny))

# 경로 추적 후 9로 표시
if found:
    x, y = end_x, end_y
    while True:
        if maze[x][y] == 0:
            maze[x][y] = 9
        if (x, y) == (start_x, start_y):
            print("프로그램왈: 사용자 이동경로 표시 완료")
            break
        x, y = parent[x][y]

#===========================================================================================================================================================================================

# 방향 설정: W, A, S, D
directions = {
    'w': (-1, 0),
    's': (1, 0),
    'a': (0, -1),
    'd': (0, 1)
}

# 시작 위치 설정
x, y = start_x, start_y
maze[x][y] = 7  # 현재 위치는 7로 표시

# 미로 원본 백업 (경로 추적용)
maze_backup = [row[:] for row in maze]

# 잘못된 8 제거 함수
def clear_wrong_path():
    for i in range(len(maze)):
        for j in range(len(maze[0])):
            if maze[i][j] in (8, 7):  # 8이나 7인 위치 중
                if (i, j) != (x, y) and (i, j) != (start_x, start_y) and maze_backup[i][j] != 9:
                    maze[i][j] = 0
                    engine.say("잘못된 경로로 가셨어요. 다시 가보세요.")

# 미로 그리기 함수
def draw_maze(stdscr):
    stdscr.clear()
    for i, row in enumerate(maze):
        for j, val in enumerate(row):
            if val == 1:
                ch = '⬜️'  # 벽 or 장애물(목적지가 아닌 건물)
            elif val == 0:
                ch = ' '   # 보행자 길
            elif val == 2:
                ch = '💙'  # 목표
            elif val == 9:
                ch = '🔘'  # 최단 경로 표시
            elif val == 8:
                ch = '🟩'  # 지나온 경로
            elif val == 7:
                ch = '🧑'  # 현재 위치(사용자)
            else:
                ch = str(val)
            stdscr.addstr(i, j * 2, ch)
    stdscr.refresh()

# 게임 실행 함수
def main(stdscr):
    global x, y
    curses.curs_set(0)
    stdscr.nodelay(True)
    draw_maze(stdscr)

    while True:
        key = stdscr.getch()
        if key == -1:
            time.sleep(0.05)
            continue

        try:
            char = chr(key).lower()
        except:
            continue

        if char in directions:
            dx, dy = directions[char]
            nx, ny = x + dx, y + dy

            if 0 <= nx < len(maze) and 0 <= ny < len(maze[0]) and maze[nx][ny] != 1:
                maze[x][y] = 8  # 이전 위치는 8로
                x, y = nx, ny

                if maze[x][y] == 2:
                    maze[x][y] = 7
                    draw_maze(stdscr)

                    stdscr.addstr(11, 0, "🎉 도착했습니다! 프로그램을 종료합니다.")
                    engine.say("도착했습니다! 안내를 종료합니다.")

                    stdscr.nodelay(False)
                    stdscr.getch()
                    break

                maze[x][y] = 7
                clear_wrong_path()
                draw_maze(stdscr)

        elif char == 'q':
            break

# curses로 실행
curses.wrapper(main)

